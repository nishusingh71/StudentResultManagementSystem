    private void updateStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateStudentActionPerformed
        // TODO add your handling code here:
        String rollno = etRollNo1.getText();
        String courses = (String) course.getSelectedItem();
        String branchs = (String) branch.getSelectedItem();
        String name = etName.getText();
        String genders = (String) gender.getSelectedItem();
        String fathername = etFatherName.getText();
        if (etRollNo1.getText().isEmpty() || etName.getText().isEmpty() || etFatherName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter All Entries!");
        } else {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Nishu1234");

                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from student where rollno='" + rollno + "'");
                if (rs.next()) {
                    st.executeUpdate("update student set course='" + courses + "',branch='" + branchs + "',name='" + name + "',gender='" + genders + "',fathername='" + fathername + "' where rollno='" + rollno + "'");
                    setVisible(false);
                    new update().setVisible(true);
                    JOptionPane.showMessageDialog(null, "Update Entry Sucessfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Student Roll Number not Valid");
                }

            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_updateStudentActionPerformed

