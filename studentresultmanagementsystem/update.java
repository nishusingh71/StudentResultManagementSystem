    private void updateResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateResultActionPerformed
        // TODO add your handling code here:
        String rollno = etRollNo2.getText();
        String DSA = etdsa.getText();
        String DBMS = etdbms.getText();
        String OS = etos.getText();
        String CN = etcn.getText();
        String SD = etsd.getText();
        if (etRollNo2.getText().isEmpty() || etdsa.getText().isEmpty() || etdbms.getText().isEmpty() || etos.getText().isEmpty() || etcn.getText().isEmpty() || etsd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter All Entries!");
        } else {

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Nishu1234");

                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from student where rollno='" + rollno + "'");
                if (rs.next()) {
                    st.executeUpdate("update result set dsa='" + DSA + "',dbms='" + DBMS + "',os='" + OS + "',cn='" + CN + "',sd='" + SD + "' where rollno='" + rollno + "'");
                    setVisible(false);
                    new update().setVisible(true);
                    JOptionPane.showMessageDialog(null, "Updated Marks!");
                } else {
                    JOptionPane.showMessageDialog(null, "Roll Not Generated!");
                }

            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_updateResultActionPerformed

